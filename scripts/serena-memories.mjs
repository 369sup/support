const canonicalSourcePaths = [
  ...agentGuidanceSourcePaths,
  "docs/architecture/module-map.json",
  "docs/architecture/architecture.md",
  "package.json",
];

function header() {
  return "<!-- Generated by scripts/serena-memories.mjs. Do not edit. -->\n";
}

function titleOf(contents) {
  return contents.match(/^#\s+(.+)$/m)?.[1] ?? "Untitled guidance";
}

export function loadSerenaMemorySources(repositoryRoot) {
  return new Map(
    canonicalSourcePaths.map((path) => {
      return [path, readFileSync(join(repositoryRoot, ...path.split("/")), "utf8")];
    }),
  );
}

export function renderSerenaMemories(sources) {
  const catalog = JSON.parse(sources.get("docs/architecture/module-map.json"));
  const packageManifest = JSON.parse(sources.get("package.json"));
  const generated = new Map();
  const prefix = header();
  const contextsBySubdomain = new Map();

  for (const context of catalog.contexts) {
    const entries = contextsBySubdomain.get(context.subdomain) ?? [];
    entries.push(context);
    contextsBySubdomain.set(context.subdomain, entries);
  }

  generated.set(
    "memory_maintenance.md",
    `${prefix}# Memory Maintenance\n\n- Shared memories are generated, reviewed, committed, and read-only.\n- Local memories are owned exclusively by the deterministic engine and are never committed.\n- The model authors only \`local/current-task\`; the engine validates candidates and renders managed local views.\n- Unknown visible local memories are quarantined without content interpretation and listed in \`local/unresolved\`.\n- Raw episodes, quarantine archives, and machine state are hidden and must never contain transcripts, payloads, logs, credentials, or customer data.\n- Shared memories summarize canonical repository authorities; they never override AGENTS or architecture documents.\n- Navigate through \`mem:core\` and use \`mem:\` references so integrity checks can detect stale links.\n- Regenerate with \`pnpm serena:memories\`; do not hand-edit generated files.\n`,
  );
  generated.set(
    "core.md",
    `${prefix}# Support Project Memory Index\n\n- Product boundary and architecture: \`mem:shared/architecture\`\n- Planned bounded contexts: \`mem:shared/module-catalog\`\n- Repository commands: \`mem:shared/commands\`\n- Instruction ownership: \`mem:shared/ownership\`\n\nCanonical repository files remain authoritative.\n`,
  );
  generated.set(
    "shared/architecture.md",
    `${prefix}# Architecture Summary\n\n${catalog.product.goal}\n\n## Excluded capabilities\n\n${catalog.excludedCapabilities.map((item) => `- ${item.name}: ${item.reason}`).join("\n")}\n\n## Deferred capabilities\n\n${catalog.deferredCapabilities.map((item) => `- ${item.name}: ${item.requires}`).join("\n")}\n\n## Authorities\n\n- \`docs/architecture/module-map.json\`: product scope and context catalog.\n- \`docs/architecture/architecture.md\`: stable architecture invariants.\n- \`apps/web/src/modules/AGENTS.md\`: context activation workflow.\n`,
  );
  generated.set(
    "shared/module-catalog.md",
    `${prefix}# Planned Module Catalog\n\n${[...contextsBySubdomain].map(([subdomain, contexts]) => {
      return `## ${subdomain}\n\n${contexts.map((context) => `- ${context.name}: ${context.kind}, ${context.implementationStatus}`).join("\n")}`;
    }).join("\n\n")}\n`,
  );
  generated.set(
    "shared/commands.md",
    `${prefix}# Repository Commands\n\n${Object.entries(packageManifest.scripts).map(([name, command]) => `- \`pnpm ${name}\`: \`${command}\``).join("\n")}\n`,
  );
  generated.set(
    "shared/ownership.md",
    `${prefix}# Instruction Ownership\n\nNearest-file precedence applies. Each file below remains authoritative only for its subtree or delegated concern.\n\n${agentGuidanceSourcePaths.map((path) => `- \`${path}\`: ${titleOf(sources.get(path))}`).join("\n")}\n`,
  );

  return generated;
}

export function writeSerenaMemories(repositoryRoot) {
  const memoryRoot = join(repositoryRoot, ".serena", "memories");
  const memories = renderSerenaMemories(loadSerenaMemorySources(repositoryRoot));
  for (const [relativePath, contents] of memories) {
    const outputPath = join(memoryRoot, ...relativePath.split("/"));
    mkdirSync(dirname(outputPath), { recursive: true });
    writeFileSync(outputPath, contents, "utf8");
  }
  return memories.size;
}

if (
  process.argv[1] !== undefined &&
  pathToFileURL(resolve(process.argv[1])).href === import.meta.url
) {
  const repositoryRoot = resolve(import.meta.dirname, "..");
  const count = writeSerenaMemories(repositoryRoot);
  console.log(`Generated ${count} shared Serena memories.`);
}
